  */
class LemmatizerSpec extends UnitSpec {

  val tokens = List("My", "first", "lecture", "started", "13", "March", "2015", ".")
  val posTags = List("PRP$", "JJ", "NN", "VBD", "CD", "NNP", "CD", ".")
  val lemmas = List("my", "#ord#", "lecture", "start", "0", "march", "0", ".")

  "process" should "produce a list of lemmas from lists of tokens and postags" in {
    val res = Lemmatiser.process(tokens,posTags)
    assert(res==lemmas)
  }
}
